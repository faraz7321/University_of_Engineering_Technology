    public final void printTableData() {
        for (int i = 0; i < lib.mag.size(); i++) {
            Magazine e = lib.mag.get(i);
            String data1 = e.getTitle();
            String data3 = e.getEditors();
            String data2 = e.getISBN();
            String data4 = e.getMonthOfPublish();
            Object[] row = {data1, data2, data3, data4};
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.addRow(row);
        }
        for (int i = 0; i < lib.tb.size(); i++) {
            TechnicalBook e = lib.tb.get(i);
            String data1 = e.getTitle();
            String data2 = e.getISBN();
            String data3 = e.getAuthors();
            String data4 = e.getEdition();
            Object[] row = {data1, data2, data3, data4};
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.addRow(row);
        }
    }

