    /**
     *
     * @param isbn
     * @return
     */
    public boolean isIsbn(String isbn) {
        char[] c = isbn.toCharArray();
        boolean flag = false;
        if (c[0] == 'I' && c[1] == 'S' && c[2] == 'B' && c[3] == 'N' && c[4] == '-') {
            for (int i = 5; i < c.length; i++) {
                if (c[i] >= '0' && c[i] <= '9') {
                    flag = true;
                }
            }

        }
        return flag;
    }

